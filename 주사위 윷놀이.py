def perm(i, v):
    global result
    if i == 10:
        result = 0
        move(v)
    else:
        for m in range(1, 5):
            v[i] = m
            perm(i+1, v)

def move(v):
    global result, ans
    horse = [0]*5
    pan = [0]*33
    for i in range(10):
        before = horse[v[i]]
        if before > 31:
            return
        else:
            after = youth[before][dice[i]]
        if after == 32 or not pan[after]:
            horse[v[i]] = after
            result += youth[after][0]
            pan[before] = 0
            pan[after] = 1
        else:
            return
    if result > ans:
        ans = result

